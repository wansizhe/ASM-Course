_TEXT	SEGMENT 'CODE'
	ASSUME CS: _TEXT, DS: _DATA
START:	MOV	AX, _DATA
	MOV	DS, AX

AGAIN:	MOV	AH, 9
	MOV	DX,OFFSET MSG
	INT	21H
	MOV	AH, 1
	INT	21H
