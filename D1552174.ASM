	JMP	ENDSWITCH
CASE7:	MOV	DX, OFFSET S7	
	JMP	ENDSWITCH
CASE0:	MOV	AX, 4C00H	
	INT	21H
ENDSWITCH:	MOV	AH, 9
		INT	21H
		JMP	AGAIN
