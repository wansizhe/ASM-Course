	ADD	AX, AX
	MOV	BX, AX
	JMP	WORD PTR CS: CASETAB [BX]
CASETAB	DW	CASE0, CASE1, CASE2, CASE3, CASE4, CASE5, CASE6, CASE7, DEFAULT
DEFAULT:MOV	DX, OFFSET ERR	
	JMP	ENDSWITCH
CASE1:	MOV	DX, OFFSET S1	
